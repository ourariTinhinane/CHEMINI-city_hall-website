              </ul>

              <p>  Son territoire présente une topographie accidentée, qui comprend deux ensembles distincts: un ensemble accidenté, correspondant
                à <strong>une zone montagneuse</strong>(montagne d'Ait Ouaghlis) avec toutes ses caractéristiques et occupe prèsque la totalité de 
                l'espace communal. Une autre zone à superficie infirme que la précédente, où les pentes sont relativement douces; c'est la partie qui
